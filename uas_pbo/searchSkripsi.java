    private void updateTable(EntityManager em) {
        Peminjaman.clear();

        // ... (kode untuk mendapatkan data dari database)
        DefaultTableModel newModel = new DefaultTableModel();
        newModel.addColumn("ID");
        newModel.addColumn("Nama");
        newModel.addColumn("ID Skripsi");
        newModel.addColumn("Judul");
        newModel.addColumn("Tanggal");
        newModel.addColumn("Jumlah");
        newModel.addColumn("Status");

        for (PeminjamanPOJO data : Peminjaman) {
            Object[] rowData = {
                data.getId(),
                data.getNama(),
                data.getIdSkripsi(),
                data.getJudulSkripsi(),
                data.getTanggal(),
                data.getJumlah(),
                data.getStatus()
            };
            newModel.addRow(rowData);
        }

    }

    private void Tampilan(Connection conn) {
        EntityManager entityManager = Persistence.createEntityManagerFactory("Uas_PboPU").createEntityManager();
        entityManager.getTransaction().begin();
        TypedQuery<Peminjaman> querySelectAll = entityManager.createNamedQuery("Peminjaman.findAll", Peminjaman.class);
        List<Peminjaman> results = querySelectAll.getResultList();

        DefaultTableModel model = (DefaultTableModel) DefaultTableModel.getModel();
        model.setRowCount(0);
        for (Peminjaman data : results) {
            Object[] baris = new Object[7];
            baris[0] = data.getId();
            baris[1] = data.getNama();
            baris[2] = data.getIdSkripsi();
            baris[3] = data.getJudulSkripsi();
            baris[4] = data.getTanggal();
            baris[5] = data.getJumlah();
            baris[6] = data.getStatus();
            model.addRow(baris);
        }
        entityManager.getTransaction().commit();
        entityManager.close();
    }

