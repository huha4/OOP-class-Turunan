    private void jSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jSearchKeyReleased
        try {
            String selection = (String) cSkripsi.getSelectedItem();
            String searchTerm = jSearch.getText().trim().toLowerCase();

            // Building the JPA query dynamically based on the selected criteria
            String queryString = "SELECT s FROM Skripsi s WHERE ";

            switch (selection.toLowerCase()) {
                case "skripsi":
                    queryString += "LOWER(s.judulSkripsi) LIKE LOWER(:searchTerm)";
                    break;
                case "pengarang":
                    queryString += "LOWER(s.pengarang) LIKE LOWER(:searchTerm)";
                    break;
                case "penerbit":
                    queryString += "LOWER(s.penerbit) LIKE LOWER(:searchTerm)";
                    break;
                case "tahun terbit":
                    queryString += "LOWER(s.tahunTerbit) LIKE LOWER(:searchTerm)";
                    break;
                default:
                    throw new IllegalArgumentException("No search criteria selected.");
            }

            // Create and execute the JPA query
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("Uas_PboPU");
            EntityManager em = emf.createEntityManager();

            TypedQuery<Peminjaman> query = em.createQuery(queryString, Peminjaman.class);
            query.setParameter("searchTerm", "%" + searchTerm + "%");

            List<Peminjaman> results = query.getResultList();

            DefaultTableModel dataModel = new DefaultTableModel();

            // Add columns to the model
            dataModel.addColumn("ID");
            dataModel.addColumn("Nama");
            dataModel.addColumn("ID Skripsi");
            dataModel.addColumn("Judul");
            dataModel.addColumn("Tanggal");
            dataModel.addColumn("Jumlah");
            dataModel.addColumn("Status");
            // ... tambahkan kolom lain sesuai kebutuhan

            // Add rows to the model
            for (Peminjaman result : results) {
                Object[] rowData = {
                    result.getId(),
                    result.getNama(),
                    result.getIdSkripsi(),
                    result.getJudulSkripsi(),
                    result.getTanggal(),
                    result.getJumlah(),
                    result.getStatus()};
                dataModel.addRow(rowData);
            }

            // Set jTableBuku with the created model
            DefaultTableModel.setModel(dataModel);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jSearchKeyReleased

